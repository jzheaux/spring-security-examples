CREATE TABLE person
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME     VARCHAR(256) NOT NULL,
    EMAIL    VARCHAR(512) NOT NULL UNIQUE
);

CREATE TABLE post
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CONTENT  VARCHAR(4096) NOT NULL,
    PERSON   BIGINT,
    FOREIGN KEY (PERSON) references person(ID)
);
